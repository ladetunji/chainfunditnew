async function seedCharities() {
  try {
    console.log('üå± Seeding charities...\n');

    for (const charity of sampleCharities) {
      try {
        await db.insert(charities).values(charity).onConflictDoUpdate({
          target: charities.slug,
          set: {
            ...charity,
            updatedAt: new Date(),
          },
        });
        console.log(`‚úÖ Added/Updated: ${charity.name}`);
      } catch (error) {
        console.error(`‚ùå Error adding ${charity.name}:`, error);
      }
    }

    console.log(`\n‚ú® Successfully seeded ${sampleCharities.length} charities!`);
  } catch (error) {
    console.error('Error seeding charities:', error);
    process.exit(1);
  }
}

seedCharities();

